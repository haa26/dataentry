    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        System.out.println(getCurrentCard().getName());
        System.out.println(bUsaha123.getName());
        card = (CardLayout) containerPanel.getLayout();

        if (getCurrentCard().getName() != null) {
            if (getCurrentCard().getName().equals("blok4")) {
                setJumlahPekerja(getJumlahPekerjaBlok4());

                if (jumlahPekerja != 0) {
                    Component[] comp = containerPanel.getComponents();
                    if (comp.length == 2) {
                        while (getLoop() < getJumlahPekerja()) {
                            BlokUsaha5 b5new = new BlokUsaha5();
                            BlokUsaha5B b5Bnew = new BlokUsaha5B();
                            BlokUsaha5C b5Cnew = new BlokUsaha5C();
                            BlokUsaha5D b5Dnew = new BlokUsaha5D();
                            b5new.setName("blok5");
                            b5Bnew.setName("blok5b");
                            b5Cnew.setName("blok5c");
                            b5Dnew.setName(String.valueOf(getLoop() + 1));
                            containerPanel.add(b5new);
                            containerPanel.add(b5Bnew);
                            containerPanel.add(b5Cnew);
                            containerPanel.add(b5Dnew);

                            b3RinciusahaTambahanListA.add(b5new.getB3RinciusahaTambahan1());
                            b3RinciusahaTambahanListB.add(b5Bnew.getB3RinciusahaTambahan1());
                            listAllB3R15.add(b5Bnew.getListModelB3R15());
                            b3RinciusahaTambahanListC.add(b5Cnew.getB3RinciusahaTambahan1());
                            b3RinciusahaTambahanListD.add(b5Dnew.getB3RinciusahaTambahan1());

                            setLoop(getLoop() + 1);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(panel, "Cek Rincian 16");
                    return;
                }
            }

            if (getCurrentCard().getName().equals("blok123")) {
                if (BlokUsaha4.getLoopPanel().getComponents().length < 1) {
                    System.out.println("mau masuk SETLOOP");
                    bUsaha4.setLoop();
                    i = 0;
                    card.next(getContainerPanel());
                    
                    jScrollPane1.getViewport().setViewPosition(new java.awt.Point(0, 0));
                    return;
                }
            }
            if (getCurrentCard().getName().matches("[0-9]")) {
                if (!(getCurrentCard().getName().equals(String.valueOf(getJumlahPekerja())))) {
                    jScrollPane1.getViewport().setViewPosition(new java.awt.Point(0, 0));
                    JOptionPane.showMessageDialog(this, "Lanjut input entri ART ke-" + String.valueOf(Integer.parseInt(getCurrentCard().getName()) + 1));

                    card.next(getContainerPanel());
                    BlokUsaha5.getB5R1NAMA().requestFocusInWindow();
                    jScrollPane1.getViewport().setViewPosition(new java.awt.Point(0, 0));
                    return;

                }

            }

            if (getCurrentCard().getName().equals(String.valueOf(jumlahPekerja))) {
                JOptionPane.showMessageDialog(this, "Sudah selesai. Silakan submit");
                return;
            }

            card.next(getContainerPanel());
            BlokUsaha5.getB5R1NAMA().requestFocusInWindow();
            
            BlokUsaha5C.getB5R18A().requestFocusInWindow();
            BlokUsaha5D.getB5R24().requestFocusInWindow();
            
            jScrollPane1.getViewport().setViewPosition(new java.awt.Point(0, 0));

        } else {
            card.next(getContainerPanel());
            jScrollPane1.getViewport().setViewPosition(new java.awt.Point(0, 0));
        }
    }//GEN-LAST:event_nextButtonActionPerformed

