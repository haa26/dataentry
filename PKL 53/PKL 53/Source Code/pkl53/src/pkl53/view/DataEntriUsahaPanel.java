    private int getJumlahPekerjaBlok4() {
        int j = 0;
        if (BlokUsaha4.getLoopPanel().getComponents().length > 0) {
            Component[] comp = BlokUsaha4.getLoopPanel().getComponents();
            for (int i = 0; i < comp.length; i++) {
                Loop4Usaha loops = (Loop4Usaha) comp[i];
                if (loops.getB4R16().getText().equals("1")) {
                    j++;
                    System.out.println("Usaha Jumlah pekerja: " + j);
                }
            }
        }
        return j;
    }

